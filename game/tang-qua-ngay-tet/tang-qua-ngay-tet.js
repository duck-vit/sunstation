document.getElementById("button_submit").onclick = function() {
    var result = document.getElementById("result").value;
    if(result == "") {
        alert("Bạn chưa nhập gì cả!");
        return;
    }
    if(result >= 16) {
        alert("Có vẻ bạn đã chọn trên 3 hộp! Vui lòng thử lại!");
        return;
    }
    if(result == 6) {
        alert("Chúc mừng! Bạn được 1 tờ giấy A4");
        document.getElementById("inner_bonus").innerHTML = "1 tờ giấy A4";
        console.log("Thêm 1 tờ A4");
        document.getElementById("main").style.display = "block";
        document.getElementById("index").style.display = "none";
        return;
    }
    if(result <= 5) {
        alert("Số bạn nhập không hợp lệ!");
        return;
    }
    if(result <= 10) {
        alert("Chúc mừng! Bạn được 2 tờ giấy A4");
        console.log("Thêm 2 tờ A4");
        document.getElementById("inner_bonus").innerHTML = "2 tờ giấy A4";
        document.getElementById("main").style.display = "block";
        document.getElementById("index").style.display = "none";
        return;
    }
    if(result <= 15) {
        alert("Chúc mừng! Bạn được 1 tờ giấy A3");
        console.log("Thêm 1 tờ A3");
        document.getElementById("inner_bonus").innerHTML = "1 tờ giấy A3";
        document.getElementById("main").style.display = "block";
        document.getElementById("index").style.display = "none";
        return;
    }
    if(result == "follow") {
        document.getElementById("follow_page").style.display = "block";
        document.getElementById("index").style.display = "none";
    }
    if(result == "follow page") {
        document.getElementById("follow_page").style.display = "block";
        document.getElementById("index").style.display = "none";
    }
    if(result == "flp") {
        document.getElementById("follow_page").style.display = "block";
        document.getElementById("index").style.display = "none";
    }
    else {
        alert("Bạn đã ghi sai cú pháp!")
    }
}

